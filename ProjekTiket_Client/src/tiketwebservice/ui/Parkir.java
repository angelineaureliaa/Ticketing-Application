    private void jDateChooser2PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooser2PropertyChange
        // TODO add your handling code here:
        try {
            String mall = "", lantai = "", tanggal = "";
            this.IsiRB();
            if (jComboBoxNamaMall.getSelectedIndex() != -1 || jComboBoxLantai.getSelectedIndex() != -1) {
                mall = jComboBoxNamaMall.getSelectedItem().toString();
                lantai = jComboBoxLantai.getSelectedItem().toString();
                Date selectedDate = jDateChooser2.getDate();
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                tanggal = dateFormat.format(selectedDate).toString();
            }
            String chatClient;
            chatClient = "Lihat Reservasi-n" + mall + "-m" + lantai + "-m" + tanggal;
            DataOutputStream out = new DataOutputStream(s.getOutputStream());
            System.out.println(chatClient);
            out.writeBytes(chatClient + "\n");
            this.getMessage();

        } catch (Exception ex) {
            System.out.println("Error di buttonSearch: belum memilih tanggal");
        }
    }//GEN-LAST:event_jDateChooser2PropertyChange


    private void jButtonPesanPark1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesanPark1ActionPerformed
        // TODO add your handling code here:\
        namaMall = jComboBoxNamaMall.getSelectedItem().toString();
        //String lokasi = "Lokasi Mall"; // Ambil lokasi mall dari sumber yang sesuai
        lantai = jComboBoxLantai.getSelectedItem().toString();
        Date selectedDate = jDateChooser2.getDate();
        System.out.println(selectedDate);
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        tanggal = dateFormat.format(selectedDate).toString();
        slotNumber = selectedSlotName;

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate tanggalHariIni = LocalDate.now();
        LocalDate parkirDate = LocalDate.parse(tanggal, formatter);

        if (parkirDate.isAfter(tanggalHariIni) || parkirDate.equals(tanggalHariIni)) {
            DetailPesanParkir form = new DetailPesanParkir();
            form.namaMall = namaMall;
            form.lantai = lantai;
            form.tanggal = tanggal;
            form.slotNumber = slotNumber;
            form.usn = usn;
            form.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Maaf, mohon pilih tanggal parkir minimal hari ini", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesanPark1ActionPerformed


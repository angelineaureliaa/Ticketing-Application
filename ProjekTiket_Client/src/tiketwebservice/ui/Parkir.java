    public void getMessage() throws IOException {
        System.out.println("mulai masuk get message");
        String chatServer;

        in = new BufferedReader(new InputStreamReader(s.getInputStream()));
        chatServer = in.readLine();

        System.out.println(chatServer);
        String[] arrayDariServer = chatServer.split("-n");

        String status = arrayDariServer[0];

        if (status.equals("Daftar Tempat")) {
            System.out.println("masuk ke mengeset");
            String textLong = arrayDariServer[1];
            String[] arrayDariTextLong = textLong.split("#");

            jComboBoxNamaMall.setModel(new DefaultComboBoxModel<>(arrayDariTextLong));
        } else if (status.equals("Gagal Daftar Tempat")) {
            System.out.println("Tidak ada daftar tempat");
        } else if (status.equals("Daftar")) {
            System.out.println("Balasan Search Button " + arrayDariServer[1]);
            String[] message = arrayDariServer[1].split("-m");
            if (message != null) {
                System.out.println("Array dari server tidak null");
                System.out.println(message[0]);
                for (String s : message) {
                    if (s.contains("A")) {
                        for (Component cp : jPanelA.getComponents()) {
                            JRadioButton rb = (JRadioButton) cp;
                            String aa = "A" + rb.getText();
                            if (aa.equals(s)) {
                                rb.setEnabled(false);
                                rb.setForeground(Color.RED);
                            }
                        }
                    } else if (s.contains("B")) {
                        System.out.println("Masuk B nih");
                        for (Component cp : jPanelB.getComponents()) {
                            JRadioButton rb = (JRadioButton) cp;
                            String aa = "B" + rb.getText();
                            if (aa.equals(s)) {
                                rb.setEnabled(false);
                                rb.setForeground(Color.RED);
                            }
                        }
                    } else if (s.contains("C")) {
                        for (Component cp : jPanelC.getComponents()) {
                            JRadioButton rb = (JRadioButton) cp;
                            String aa = "C" + rb.getText();
                            if (aa.equals(s)) {
                                rb.setEnabled(false);
                                rb.setForeground(Color.RED);
                            }
                        }
                    }
                }
            }
        }
    }

